(function (window, document, $) {

  "use strict";

  /**
   * ---------------------------------------------------------------------------
   *  Variable
   * ---------------------------------------------------------------------------
   */
  var $page = $("#page-home");
  var $talkTime = $(".talk-time");




  /**
   * ---------------------------------------------------------------------------
   *  Init
   * ---------------------------------------------------------------------------
   */


  /**
   * ---------------------------------------------------------------------------
   *  Event Binding
   * ---------------------------------------------------------------------------
   */
  $page.one("open", HANDLER_showWelcome);



  /**
   * ---------------------------------------------------------------------------
   *  Event Handler
   * ---------------------------------------------------------------------------
   */
  function HANDLER_showWelcome(event) {
    RENDER_talkTime();
  }



  /**
   * ---------------------------------------------------------------------------
   *  Business Logic
   * ---------------------------------------------------------------------------
   */




  /**
   * ---------------------------------------------------------------------------
   *  Content Render
   * ---------------------------------------------------------------------------
   */
  function RENDER_talkTime() {
    var now = new Date();
    var hour = now.getHours();
    var min = now.getMinutes();
    $talkTime[0].innerHTML = format(hour) + ":" + format(min);
  }

  function format(num) {
    var str = String(num);
    if (str.length === 1) {
      return "0" + str;
    }
    return str;
  }


  /**
   * ---------------------------------------------------------------------------
   *  External API
   * ---------------------------------------------------------------------------
   */



})(window, document, $);